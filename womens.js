        brand.style.color = "green"
        brand.style.textDecoration = "underline"
        let Pname = document.createElement("p")
        Pname.innerText = el.name
        let box1 = document.createElement("div")
        let Sprice =document.createElement("h3")
        Sprice = "$"+el.price
        let Pprice =document.createElement("h2")
        Pprice.innerText ="$"+ el.strikedoffprice
        
        box1.append(Sprice,Pprice)
        let btn = document.createElement("button")
